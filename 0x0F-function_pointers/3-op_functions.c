#include "3-calc.h"

/**
 * op_add - calculate the sum
 * @a: first
 * @b: second
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculate difference
 * @a: firsy
 * @b: second
 *
 * Return: diffrence of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates multiply
 * @a: first
 * @b: second
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division
 * @a: first
 * @b: second
 *
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates
 * @a: first
 * @b: second
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

